package recipeMall.recipe;

public class RecipeInfoVO {
	private int recipeInfoNo;
	private String infoServ;
	private String infoTime;
	private String infoDiff;
	
	
	
	
	public int getRecipeInfoNo() {
		return recipeInfoNo;
	}
	public void setRecipeInfoNo(int recipeInfoNo) {
		this.recipeInfoNo = recipeInfoNo;
	}
	public String getInfoServ() {
		return infoServ;
	}
	public void setInfoServ(String infoServ) {
		this.infoServ = infoServ;
	}
	public String getInfoTime() {
		return infoTime;
	}
	public void setInfoTime(String infoTime) {
		this.infoTime = infoTime;
	}
	public String getInfoDiff() {
		return infoDiff;
	}
	public void setInfoDiff(String infoDiff) {
		this.infoDiff = infoDiff;
	}
	
	
}
